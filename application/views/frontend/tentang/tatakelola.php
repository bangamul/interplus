							<h3>TRANSPARANSI (transparency)</h3>
							<p>Transparansi adalah keterbukaan dalam melaksanakan proses pengambilan keputusan dan dalam mengemukakan informasi materiil dan relevan mengenai Perusahaan secara akurat dan tepat waktu. Hal-hal yang harus diperhatikan dalam melaksanakan prinsip transparansi adalah pengungkapan informasi oleh Perusahaan dilakukan dengan:</p>
							<p>Mematuhi Anggaran Dasar, peraturan perundang- undangan yang berlaku, Peraturan Perusahaan dan prinsip-prinsip GCG. Menyediakan informasi baik informasi yang wajib, sukarela tetapi menjadi nilai tambah bagi Perusahaan dan tidak mengurangi kewajiban Perusahaan untuk memenuhi ketentuan kerahasiaan kepada Pemegang Saham dan stakeholders secara akurat dan tepat waktu, serta mudah diakses sesuai dengan batasan yang ditetapka Perusahaan.</p>
						</li>
						<li>
							<h3>AKUNTABILITAS (accountability)</h3>
							<p>Akuntabilitas adalah kejelasan fungsi, pelaksanaan dan pertanggungjawaban organ Perseroan maupun pegawai sehingga pengelolaan perusahaan dapat dilaksanakan secara efektif. Pada prinsip ini, PT HOGAN GAILANG INTERPLUS (HGI) mengenal 3 (tiga) jenis tingkatan akuntabilitas dalam setiap aktivitas Perseroan, yang meliputi:</p>
							<ol type="1" style="padding-inline-start: 20px">
								<li>
									<h4>Akuntabilitas Individual</h4>
									<p>Akuntabilitas individual merujuk kepada hubungan akuntabilitas dalam konteks atasan bawahan. Akuntabilitas berlaku kepada para pihak, baik yang mempunyai wewenang maupun yang mendapatkan penugasan dari pemegang wewenang (pelimpahan tugas). Pemegang wewenang bertanggungjawab untuk memberikan arahan, bimbingan dan sumberdaya yang diperlukan serta membantu menghilangkan kendala yang dapat mempengaruhi kinerja. Pelaksana tugas bertanggungjawab terhadap penyelesaian hasil atau sasaran atas penugasan dan atau pelimpahan kewenangan yang diperolehnya. Dalam konteks ini kedua belah pihak mempunyai akuntabilitas masing- masing.</p>
								</li>
								<li>
									<h4>Akuntabilitas Unit Kerja/Tim</h4>
									<p>Akuntabilitas Unit Kerja/Tim merujuk kepada adanya akuntabilitas yang ditanggung bersama oleh suatu Unit Kerja/Tim atas pencapaian/tidak tercapainya tugas yang diterima. Dalam hal Unit Kerja/Tim menyampaikan laporan, maka harus dibedakan antara akuntabilitas individu dan Unit Kerja/Tim.</p>
								</li>
								<li>
									<h4>Akuntabilitas Korporasi</h4>
									<p>Akuntabilitas korporasi merujuk kepada akuntabilitas Perusahaan. Dalam menjalankan peranan sebagai entitas usaha, PT HOGAN GAILANG INTERPLUS (HGI) bertanggungjawab atas aktivitas bisnis yang dijalankannya. Setiap Organ Perusahaan dapat dimintai akuntabilitas masing-masing sesuai tugas dan tanggung jawabnya dengan mengacu kepada peraturan perundang-undangan, kebijakan Perusahaan, peraturan-peraturan Perusahaan dan ketentuan lainnya.</p>
								</li>
							</ol>
							<p>Hal-hal yang harus diperhatikan dalam melaksanakan prinsip Akuntabilitas adalah:</p>
							<p>Menetapkan tanggung jawab yang jelas dari masing-masing Insan PT HOGAN GAILANG INTERPLUS (HGI) yang sejalan dengan visi dan misi Perusahaan termasuk kebijakan yang mendukung pelaksanaan tugas dan kewajiban sesuai peraturan perundang-undangan yang berlaku.</p>
							<p>Melaksanakan tugas dan kewajiban untuk kepentingan Perusahaan baik secara individu, unit kerja /tim dan korporasi. Mempertanggungjawabkan pelaksanaan tugas berdasarkan ukuran kinerja yang telah ditetapkan Perusahaan dengan tepat waktu.</p>
						</li>
						<li>
							<h3>PERTANGGUNG JAWABAN (responsibility)</h3>
							<p>Pertanggungjawaban adalah kesesuaian pengelolaan Perusahaan terhadap peraturan perundang- undangan yang berlaku termasuk peraturan dan kebijakan Perusahaan, dan prinsip-prinsip korporasi yang sehat. Pertanggungjawaban juga diikuti dengan komitmen untuk menjalankan aktivitas bisnis sesuai dengan standar etika (kode etik). Hal-hal yang harus diperhatikan dalam melaksanakan prinsip pertanggungjawaban adalah menjadikan Perusahaan sebagai good corporate citizen yang antara lain diwujudkan dengan:</p>
							<p>Pemenuhan kewajiban terhadap Regulator, International Federation of Inspection Agencies (IFIA) dan Pemegang Saham secara tepat waktu. Pengelolaan lingkungan sesuai standar yang ditetapkan oleh peraturan perundang-undangan; dan, Perlindungan terhadap hak-hak stakeholders secara umum. Kewajiban Perusahaan dalam memperhatikan kepentingan stakeholders lainnya.</p>
						</li>
						<li>
							<h3>KEMANDIRIAN (independency)</h3>
							<p>Kemandirian adalah suatu keadaan dimana Perusahaan dikelola secara profesional tanpa benturan kepentingan dan atau pengaruh/tekanan dari pihak manapun yang tidak sesuai dengan perundang-undangan yang berlaku dan prinsip-prinsip korporasi yang sehat. Hal-hal yang harus diperhatikan dalam melaksanakan prinsip kemandirian adalah:</p>
							<p>Mengambil keputusan secara objektif berdasarkan data dan informasi yang dapat dipertanggungjawabkan serta bebas dari kepentingan individu, kelompok maupun golongan tertentu</p>
							<p>Menghormati hak dan tanggung jawab masing-masing Organ Perusahaan sesuai dengan Anggaran Dasar dan peraturan perundang-undangan yang berlaku. Kemandirian menjadi penting agar masing-masing organ Perusahaan dapat menjalankan tugas dengan sebaik-baiknya untuk kepentingan Perusahaan dan dapat dimintai akuntabilitas atas pelaksanaan tugas masing-masing.</p>
						</li>
						<li>
							<h3>KEWAJARAN (fairness)</h3>
							<p>Kewajaran adalah keadilan dan kesetaraan dalam memenuhi hak-hak stakeholders yang timbul berdasarkan perjanjian perundang-undangan, kebijakan Perusahaan, peraturan peraturan Perusahaan dan ketentuan lainnya serta prinsip-prinsip korporasi yang sehat. Hal-hal yang harus diperhatikan dalam melaksanakan prinsip kewajaran adalah:</p>
							<p>Memberikan informasi kepada Pemegang Saham sesuai dengan haknya atau tanpa membedakan jumlah kepemilikan saham. Memberikan kesempatan yang sama tanpa diskriminasi kepada stakeholders untuk menyampaikan masukan, pendapat bagi kepentingan Perusahaan sesuai dengan mekanisme yang ditetapkan Perusahaan. Memberikan reward dan punishment sesuai dengan kebijakan yang berlaku.</p>
						</li>
					</ol>
					<p>Fokus kami memberikan layanan sertifikasi untuk berbagai sistem manajemen perusahaan. Sebagai perusahaan yang termasuk dalam memajukan standar kualitas dan keamanan di berbagai sektor industri, kami telah membangun reputasi yang solid sebagai mitra terpercaya untuk perusahaan-perusahaan di seluruh dunia.</p>
